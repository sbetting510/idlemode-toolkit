// ── Ability Score Modifier ────────────────────────────────────────────────────
export function abilityMod(score) {
  return Math.floor((score - 10) / 2)
}

export function modStr(score) {
  const m = abilityMod(score)
  return (m >= 0 ? '+' : '') + m
}

